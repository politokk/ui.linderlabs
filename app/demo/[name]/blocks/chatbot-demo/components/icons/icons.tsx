export function getIconForLanguageExtension(language: string) {
  switch (language) {
    case "json":
      return <Icons.json />
    case "css":
      return <Icons.css className="fill-foreground" />
    case "js":
    case "jsx":
    case "ts":
    case "tsx":
    case "typescript":
      return <Icons.ts className="fill-foreground" />
    default:
      return <FileIcon />
  }
}
